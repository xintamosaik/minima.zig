/// Initializes world state.
export fn init() void {
    var ty: u32 = 0;
    while (ty < constants.GRID_H) : (ty += 1) {
        var tx: u32 = 0;
        while (tx < constants.GRID_W) : (tx += 1) {
            const use_light = ((tx + ty) & 1) == 0;
            setTile(tx, ty, if (use_light) .light else .dark);
        }
    }
}
