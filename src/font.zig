inline fn getGlyph(c: u8) [8]u8 {
    return switch (c) {
        // Letters
        'A' => A,
        'B' => B,
        'C' => C,
        'D' => D,
        'E' => E,
        'F' => F,
        'G' => G,
        'H' => H,
        'I' => I,
        'J' => J,
        'K' => K,
        'L' => L,
        'M' => M,
        'N' => N,
        'O' => O,
        'P' => P,
        'Q' => Q,
        'R' => R,
        'S' => S,
        'T' => T,
        'U' => U,
        'V' => V,
        'W' => W,
        'X' => X,
        'Y' => Y,
        'Z' => Z,

        // Numbers
        '0' => N0,
        '1' => N1,
        '2' => N2,
        '3' => N3,
        '4' => N4,
        '5' => N5,
        '6' => N6,
        '7' => N7,
        '8' => N8,
        '9' => N9,

        // Punctuation
        '.' => DOT,
        ',' => COMMA,
        ':' => COLON,
        ';' => SEMICOLON,
        '!' => EXCLAMATION,
        '?' => QUESTION,
        '-' => DASH,
        '+' => PLUS,
        '/' => SLASH,
        ' ' => SPACE,

        // Optional: lowercase fallback → uppercase
        'a' => A,
        'b' => B,
        'c' => C,
        'd' => D,
        'e' => E,
        'f' => F,
        'g' => G,
        'h' => H,
        'i' => I,
        'j' => J,
        'k' => K,
        'l' => L,
        'm' => M,
        'n' => N,
        'o' => O,
        'p' => P,
        'q' => Q,
        'r' => R,
        's' => S,
        't' => T,
        'u' => U,
        'v' => V,
        'w' => W,
        'x' => X,
        'y' => Y,
        'z' => Z,

        else => SPACE,
    };
}

pub fn drawChar(
    x: u32,
    y: u32,
    c: u8,
    fg: u32,
    bg: u32,
) void {
    const glyph = getGlyph(c);
    renderer.drawBitmap8x8(x, y, glyph, fg, bg);
}

pub fn drawString(
    x: u32,
    y: u32,
    text: []const u8,
    fg: u32,
    bg: u32,
) void {
    var cx = x;

    for (text) |c| {
        drawChar(cx, y, c, fg, bg);
        cx += 8;
    }
}
